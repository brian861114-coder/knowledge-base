def parse_frontmatter(text: str) -> tuple[str, str]:
    text = text.lstrip("\ufeff")
    if not text.startswith("---\n"):
        return "", text
    end = text.find("\n---\n", 4)
    if end == -1:
        return "", text
    return text[4:end], text[end + 5 :]


def parse_frontmatter_data(raw: str) -> dict[str, str]:
    data: dict[str, str] = {}
    for line in raw.splitlines():
        if ":" not in line:
            continue
        key, value = line.split(":", 1)
        data[key.strip()] = value.strip().strip("'\"")
    return data


def replace_intuition_section(body: str, new_text: str) -> tuple[str, bool]:
    match = re.search(rf"^##\s+{re.escape(INTUITION_HEADING)}\s*$", body, re.MULTILINE)
    if not match:
        return body, False
    next_match = SECTION_PATTERN.search(body, match.end())
    start = match.start()
    end = next_match.start() if next_match else len(body)
    replacement = f"## {INTUITION_HEADING}\n{new_text.strip()}\n\n"
    return body[:start] + replacement + body[end:].lstrip(), True


def process_file(path: Path) -> bool:
    text = path.read_text(encoding="utf-8")
    raw_frontmatter, body = parse_frontmatter(text)
    frontmatter = parse_frontmatter_data(raw_frontmatter)
    title = frontmatter.get("title", path.stem).strip() or path.stem
    new_text = CORE_INTUITION_OVERRIDES.get(title)
    if not new_text:
        return False
    new_body, changed = replace_intuition_section(body, new_text)
    if not changed:
        return False
    if raw_frontmatter:
        final = f"---\n{raw_frontmatter}\n---\n{new_body}"
    else:
        final = new_body
    path.write_text(final, encoding="utf-8")
    return True


def main() -> None:
    parser = argparse.ArgumentParser(description="Deepen the intuition sections for selected core notes.")
    parser.add_argument("--vault", help="Override vault path")
    args = parser.parse_args()

    vault = resolve_vault_path(args.vault)
    changed = 0
    for path in sorted(vault.rglob("*.md")):
        if process_file(path):
            changed += 1
    print(f"changed={changed}")
    print(f"targets={len(CORE_INTUITION_OVERRIDES)}")


if __name__ == "__main__":
    main()
