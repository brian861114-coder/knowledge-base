WIKILINK_RE = re.compile(r"\[\[([^\]|#]+)(?:#[^\]|]+)?(?:\|([^\]]+))?\]\]")
BLOCK_FORMULA_RE = re.compile(r"^(.*?[：:])\s*\\\((.+)\\\)\s*[。.]?$")


def strip_wikilink(value: str) -> str:
    cleaned = value.strip().strip("'\"")
    match = WIKILINK_RE.fullmatch(cleaned)
    if not match:
        return cleaned
    return (match.group(2) or match.group(1)).strip()


def parse_list(raw: str) -> list[str]:
    raw = raw.strip()
    if raw == "[]":
        return []
    if not (raw.startswith("[") and raw.endswith("]")):
        return [strip_wikilink(raw)] if raw else []
    inner = raw[1:-1].strip()
    if not inner:
        return []
    items: list[str] = []
    for part in inner.split(","):
        item = strip_wikilink(part)
        if item:
            items.append(item)
    return items


def parse_frontmatter(text: str) -> tuple[str, dict[str, object], str]:
    if not text.startswith("---\n"):
        return "", {}, text
    end = text.find("\n---\n", 4)
    if end == -1:
        return "", {}, text
    raw = text[4:end]
    body = text[end + 5 :]
    data: dict[str, object] = {}
    for line in raw.splitlines():
        if ":" not in line:
            continue
        key, value = line.split(":", 1)
        key = key.strip()
        value = value.strip()
        if value.startswith("[") and value.endswith("]"):
            data[key] = parse_list(value)
        else:
            data[key] = value.strip("'\"")
    return raw, data, body


def bullet_links(items: list[str], fallback: str) -> str:
    if not items:
        return f"- {fallback}"
    return "\n".join(f"- [[{item}]]" for item in items)


def serial_links(items: list[str], fallback: str) -> str:
    if not items:
        return fallback
    if len(items) == 1:
        return f"[[{items[0]}]]"
    if len(items) == 2:
        return f"[[{items[0]}]] 與 [[{items[1]}]]"
    return "、".join(f"[[{item}]]" for item in items[:-1]) + f" 與 [[{items[-1]}]]"


def fix_latex_escapes(text: str) -> str:
    return (
        text.replace("\a", r"\a")
        .replace("\b", r"\b")
        .replace("\f", r"\f")
        .replace("\t", r"\t")
        .replace("\v", r"\v")
    )


def to_obsidian_math(text: str) -> str:
    text = fix_latex_escapes(text)
    text = text.replace(r"\[", "$$").replace(r"\]", "$$")
    text = text.replace(r"\(", "$").replace(r"\)", "$")
    return text


def render_formula_items(items: list[str]) -> str:
    rendered: list[str] = []
    for item in items:
        item = fix_latex_escapes(item)
        match = BLOCK_FORMULA_RE.match(item)
        if match and item.count(r"\(") == 1 and item.count(r"\)") == 1:
            label = match.group(1)
            formula = match.group(2).strip()
            rendered.append(f"{label}\n$$\n{formula}\n$$")
        else:
            rendered.append(f"- {to_obsidian_math(item)}")
    return "\n\n".join(rendered)


def render_ordered_items(items: list[str]) -> str:
    return "\n".join(f"{index + 1}. {to_obsidian_math(item)}" for index, item in enumerate(items))


def render_bullets(items: list[str]) -> str:
    return "\n".join(f"- {to_obsidian_math(item)}" for item in items)


def contains_any(text: str, keywords: list[str]) -> bool:
    return any(keyword in text for keyword in keywords)


@dataclass
class DetailSpec:
    definition: str
    formulas: list[str]
    derivation: list[str]
    examples: list[str]
    pitfalls: list[str]
    advanced: str
    why_needed: str | None = None
    measures: str | None = None
    theorem_links: str | None = None


def make_spec(
    definition: str,
    formulas: list[str],
    derivation: list[str],
    examples: list[str],
    pitfalls: list[str],
    advanced: str,
    why_needed: str | None = None,
    measures: str | None = None,
    theorem_links: str | None = None,
) -> DetailSpec:
    return DetailSpec(definition, formulas, derivation, examples, pitfalls, advanced, why_needed, measures, theorem_links)


