def unique_links(sections, order, bridges):
    items = OrderedDict()
    for _title, _intro, links in sections:
        for link in links:
            items.setdefault(link, None)
    for link in order:
        items.setdefault(link, None)
    for sentence in bridges:
        for note_id in NOTE_DETAILS:
            token = f"[[{note_id}]]"
            if token in sentence:
                items.setdefault(note_id, None)
    return list(items.keys())


def bullet(note_id: str) -> str:
    return f"- [[{note_id}]]：{describe(note_id)}"


def build_map(title: str, cfg: dict) -> str:
    includes = unique_links(cfg["sections"], cfg["order"], cfg["bridges"])
    frontmatter = [
        "---",
        "type: map",
        f"taxonomy_domain: {cfg['taxonomy_domain']}",
        f"title: {title}",
        f"summary: {cfg['summary']}",
        f"focus_domain: {cfg['focus_domain']}",
        "includes: [" + ", ".join(f'"[[{x}]]"' for x in includes) + "]",
        "recommended_order: [" + ", ".join(f'"[[{x}]]"' for x in cfg["order"]) + "]",
        "tags: [" + ", ".join(cfg["tags"]) + "]",
        "updated: 2026-06-04",
        "---",
        "",
        f"# {title}",
        "",
        "## 地圖摘要",
        cfg["overview"],
        "",
    ]

    body = []
    for section_title, intro, links in cfg["sections"]:
        body.append(f"## {section_title}")
        body.append(intro)
        body.append("")
        for link in links:
            body.append(bullet(link))
        body.append("")

    body.append("## 關係主線")
    for line in cfg["relations"]:
        body.append(f"- {line.strip()}")
    body.append("")

    body.append("## 建議閱讀順序")
    for index, link in enumerate(cfg["order"], start=1):
        body.append(f"{index}. [[{link}]]：{describe(link)}")
    body.append("")

    body.append("## 與其他地圖的橋接")
    for line in cfg["bridges"]:
        body.append(f"- {line}")
    body.append("")

    body.append("## 使用方式")
    body.append("這張系統圖的目的不是取代各節點，而是讓你先抓到主幹，再決定要往哪個分支深挖。若你已經在某一頁卡住，回來看它在整張圖中的位置，通常會比硬背公式更有效。")
    body.append("")

    return "\n".join(frontmatter + body).rstrip() + "\n"


def write_file(vault: Path, title: str, content: str) -> None:
    path = vault / "00_maps" / f"{title}.md"
    path.write_text(content, encoding="utf-8")


def main() -> None:
    vault = resolve_vault_path()
    for title, cfg in MAP_CONFIGS.items():
        content = build_map(title, cfg)
        write_file(vault, title, content)
    print(f"wrote={len(MAP_CONFIGS)} maps")


if __name__ == "__main__":
    main()
