def insert_symbol_section(text: str, section: str) -> str:
    if "## 符號說明與單位" in text:
        return text
    marker = "## 數學表述"
    start = text.find(marker)
    if start == -1:
        return text

    search_from = start + len(marker)
    next_heading = text.find("\n## ", search_from)
    if next_heading == -1:
        next_heading = len(text)

    insertion = "\n" + section.strip() + "\n"
    return text[:next_heading].rstrip() + "\n\n" + section.strip() + "\n\n" + text[next_heading:].lstrip("\n")


def main() -> None:
    vault = Path(r"C:\Users\brian\Downloads\Obsidian Vault備份\obsidian\Project\knowledge database\01_laws")
    updated: list[str] = []

    for path in sorted(vault.glob("*.md")):
        title = path.stem
        section = SYMBOL_SECTIONS.get(title)
        if not section:
            continue
        original = path.read_text(encoding="utf-8")
        rewritten = insert_symbol_section(original, section)
        if rewritten != original:
            path.write_text(rewritten, encoding="utf-8")
            updated.append(title)

    print(f"Updated {len(updated)} law notes.")
    for title in updated:
        print(f"- {title}")


if __name__ == "__main__":
    main()
